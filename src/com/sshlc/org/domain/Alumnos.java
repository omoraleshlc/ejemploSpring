package com.sshlc.org.domain;

public class Alumnos {

	private Integer keyAEMain;
	private String escuela;
	private Integer matricula;
        private String nombre;
        private String tipoAlumno;

    /**
     * @return the keyAEMain
     */
    public Integer getKeyAEMain() {
        return keyAEMain;
    }

    /**
     * @param keyAEMain the keyAEMain to set
     */
    public void setKeyAEMain(Integer keyAEMain) {
        this.keyAEMain = keyAEMain;
    }

    /**
     * @return the escuela
     */
    public String getEscuela() {
        return escuela;
    }

    /**
     * @param escuela the escuela to set
     */
    public void setEscuela(String escuela) {
        this.escuela = escuela;
    }

    /**
     * @return the matricula
     */
    public Integer getMatricula() {
        return matricula;
    }

    /**
     * @param matricula the matricula to set
     */
    public void setMatricula(Integer matricula) {
        this.matricula = matricula;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the tipoAlumno
     */
    public String getTipoAlumno() {
        return tipoAlumno;
    }

    /**
     * @param tipoAlumno the tipoAlumno to set
     */
    public void setTipoAlumno(String tipoAlumno) {
        this.tipoAlumno = tipoAlumno;
    }

   
    
}
